int	**quote(char *cmd)
{
	int	i;
	int	j;
	int k;
	int jj[3][100];
	char *cmd2;

	i = 0;
	j = 0;
	k = 0;
	while (cmd[i])
	{
		if (cmd[i] == "'")
		{
			jj[1][j] = i;
			j++;
		}
		if (cmd[i] == '"')
		{
			jj[0][k] = i;
			k++;
		}
		i++;
	}
	if (j % 2 == 0 || k % 2 == 0)
	{
		if (jj[1][0] < jj[0][0] && jj[1][j - 1] > jj[0][k - 1])
		{
			jj[2][0] = 2;
			jj[2][1] = jj[1][0];
			jj[2][2] = jj[1][j - 1];
			return (jj);
		}
		else if (jj[1][0] > jj[0][0] && jj[1][j - 1] < jj[0][k - 1])
		{
			jj[2][0] = 2;
			jj[2][1] = jj[0][0];
			jj[2][2] = jj[0][k - 1];
			return (jj);
		}
		else if (jj[1][0] > jj[0][k - 1] || jj[0][0] > jj[1][j - 1])
		{
			jj[2][0] = 4;
			jj[2][1] = jj[1][0];
			jj[2][2] = jj[1][j - 1];
			jj[2][3] = jj[0][0];
			jj[2][4] = jj[1][k - 1];
			return (jj);
		}
	}
	if (j % 2 == 1 || k % 2 == 1)
	{
		if (jj[1][j - 1] > jj[0][0] && jj[1][j - 1] < jj[0][k - 1])
		{
			jj[2][0] = 2;
			jj[2][1] = jj[0][0];
			jj[2][1] = jj[0][k - 1];
			return (jj);
		}
		else if (jj[0][k - 1] > jj[1][0] && jj[0][k - 1] < jj[1][j - 1])
		{
			jj[2][0] = 2;
			jj[2][1] = jj[1][0];
			jj[2][1] = jj[1][j - 1];
			return (jj);
		}
	}
	return (NULL);
}